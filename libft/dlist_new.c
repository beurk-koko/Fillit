#include "libft.h"

dlist			*dlist_new(void)
{
	dlist		*new;

	new = NULL;
	if ((new = (dlist*)malloc(sizeof(dlist))))
	{
		new->len = 0;
		new->head = NULL;
	}
	return (new);
}
