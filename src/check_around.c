int			check_around(char *str, int i, char c)
{
	int		count;

	count = 0;
	if (i - 5 >= 0 && i - 5 <= 19 && str[i - 5] == c)
		count++;
	if (i + 5 >= 0 && i + 5 <= 19 && str[i + 5] == c)
		count++;
	if (i - 1 >= 0 && i - 1 <= 19 && str[i - 1] == c)
		count++;
	if (i + 1 >= 0 && i + 1 <= 19 && str[i + 1] == c)
		count++;
	return (count);
}
