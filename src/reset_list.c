#include "fillit.h"

void			reset_list(t_dlist *list)
{
	t_dcell		*tmp;

	tmp = list->head;
	while (tmp->next != list->head)
	{
		tmp->put = 0;
		tmp = tmp->next;
	}
	tmp->put = 0;
}
